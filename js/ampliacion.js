const params = new URLSearchParams(window.location.search);
const id = params.get("id");

const trabajo = trabajos[id];

document.getElementById("titulo").textContent = trabajo.titulo;
document.getElementById("background").style.backgroundImage = `url(${trabajo.background})`;
document.querySelector("video").src = trabajo.video;

//texto
const textoContainer = document.getElementById("texto");
textoContainer.innerHTML = "";

trabajo.descripcion.forEach(bloque => {
  if (bloque.tipo === "p") {
    const p = document.createElement("p");
    p.textContent = bloque.contenido;
    textoContainer.appendChild(p);
  }

  if (bloque.tipo === "img") {
    const img = document.createElement("img");
    img.src = bloque.src;
    img.classList.add("mobile-only");
    textoContainer.appendChild(img);
  }
});


//tabs
const techContainer = document.getElementById("tecnologias");

trabajo.tecnologias.forEach(tech => {
  const p = document.createElement("p");
  p.textContent = tech;
  techContainer.appendChild(p);
});
